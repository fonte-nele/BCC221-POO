    public void finaliza(boolean isVencedor) {

        Util.gravaArquivo(pontos >= hiScore ? "" + pontos : "" + hiScore);



        if (isVencedor) {
            JOptionPane.showMessageDialog(this, "Parabéns! \nVocê GANHOU!!!\n\n Total de Pontos: " + pontos + "\n " + (pontos >= hiScore ? "Você bateu um RECORDE! " + pontos : ""), "GANHOU!", JOptionPane.INFORMATION_MESSAGE);


            System.exit(0);
        } else {
            JOptionPane.showMessageDialog(this, "Você errou! \n Total de Pontos: " + pontos + "\n " + (pontos >= hiScore ? "Você bateu um RECORDE! " + pontos : ""), "ERROU!", JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }


    }

