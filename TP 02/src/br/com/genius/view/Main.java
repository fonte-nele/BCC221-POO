    private void carregaRecorde() 
    {
        String recorde = Util.leArquivo();

        if (recorde != null && !recorde.isEmpty()) {
            hiScore = Integer.valueOf(recorde.trim());
        } else {
            hiScore = 0;
            Util.gravaArquivo("" + pontos);
        }
        mostraRecorde();
    }

    public void paintCampo(AbstractButton botao) 
    {
        if (botao.getName().equals("bt1")) {
            botao.setBackground(Util.COR_BOTAO_1_CLICADO);
        } else if (botao.getName().equals("bt2")) {
            botao.setBackground(Util.COR_BOTAO_2_CLICADO);
        } else if (botao.getName().equals("bt3")) {
            botao.setBackground(Util.COR_BOTAO_3_CLICADO);
        } else if (botao.getName().equals("bt4")) {
            botao.setBackground(Util.COR_BOTAO_4_CLICADO);
        }
    }

